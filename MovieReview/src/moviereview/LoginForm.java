    private void cmdLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLoginActionPerformed
        
        ResultSet rs;
        
        
        String query = "SELECT * FROM user WHERE username = ? AND password =?";
        
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/moviereview","root","");
            ps = con.prepareStatement(query);
            
            ps.setString(1, txtUsername.getText());
            ps.setString(2, String.valueOf(txtPassword.getPassword()));
            
            rs = ps.executeQuery();
            
            if(rs.next())
            {
                    MovieListForm m = new MovieListForm();
                    m.setVisible(true);
                    m.pack();
                    m.setLocationRelativeTo(null);
                    m.setExtendedState(MovieListForm.MAXIMIZED_BOTH);
                   
                    
                    this.dispose();
            }
            else{
                    JOptionPane.showMessageDialog(null, "Incorrect Username Or Password", "Login Failed", 2);
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_cmdLoginActionPerformed

